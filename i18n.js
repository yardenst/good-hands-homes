const LANGS = ["en", "es", "el", "fr", "de"];

function buildMarquee(items) {
  const seq = items.concat(items); // duplicate for seamless scroll
  return seq.map((p) => `<span>${p}</span><span class="dot">•</span>`).join("");
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.en;
  const en = I18N.en;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = dict[key] != null ? dict[key] : en[key];
    if (val != null) el.innerHTML = val;
  });

  document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
    const key = el.getAttribute("data-i18n-ph");
    const val = dict[key] != null ? dict[key] : en[key];
    if (val != null) el.setAttribute("placeholder", val);
  });

  const track = document.getElementById("marquee-track");
  if (track) track.innerHTML = buildMarquee(dict.marquee || en.marquee);

  if (dict._title) document.title = dict._title;
  const meta = document.querySelector('meta[name="description"]');
  if (meta && dict._desc) meta.setAttribute("content", dict._desc);

  const sel = document.getElementById("lang-select");
  if (sel) sel.value = lang;

  try { localStorage.setItem("gh_lang", lang); } catch (e) {}
}

(function initLang() {
  let lang = null;
  try { lang = localStorage.getItem("gh_lang"); } catch (e) {}
  if (!lang) {
    const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
    lang = LANGS.includes(nav) ? nav : "en";
  }
  const sel = document.getElementById("lang-select");
  if (sel) sel.addEventListener("change", (e) => applyLang(e.target.value));
  applyLang(lang);
})();
